export interface VideoAnalysisPromptInput {
  durationSeconds?: number;
  language?: "zh-CN";
  speechTranscript?: string;
  productName?: string;
  productImageCount?: number;
  demand?: string;
}

export function buildVideoAnalysisPrompt(input: VideoAnalysisPromptInput = {}) {
  const durationHint = input.durationSeconds
    ? `已知容器时长约为 ${input.durationSeconds.toFixed(2)} 秒；仍需以画面和音频的真实边界为准。`
    : "请从视频中自行确认总时长。";
  const transcriptHint = input.speechTranscript?.trim()
    ? `\n## 已核验原声音轨转写\n${input.speechTranscript.trim()}\n\n这份转写用于确认原意和营销信息，不要求在最终口播中逐字照抄。`
    : "";
  const businessContext = [
    input.productName?.trim() ? `商品库名称：${input.productName.trim()}` : "",
    input.productImageCount
      ? `商品参考图：已随视频同时提供 ${input.productImageCount} 张，必须综合比对全部图片后提取商品外观。`
      : "",
    input.demand?.trim() ? `用户补充需求：${input.demand.trim()}` : "",
  ]
    .filter(Boolean)
    .join("\n");
  const businessHint = businessContext
    ? `\n## 业务上下文\n${businessContext}\n商品参考图用于确认颜色、材质、结构、Logo 与原有印刷文字，视频用于确认人物动作、商品使用状态和场景；应综合全部证据，不得只看第一张图。用户文字只用于消歧和确定创作方向；若与可见事实冲突，以视频与参考图为准。`
    : "";

  return `你是一名短视频导演、视听语言分析师和电商口播脚本反解析专家。你的任务不是概括视频，而是把输入视频逆向拆解成一份可用于复刻原视频的中文生成提示词。

${durationHint}
${transcriptHint}
${businessHint}

## 分析方法（只在内部执行，不要输出分析过程）
1. 完整观看并聆听视频至少两遍：第一遍建立全局人物、商品、场景、光线、声音和镜头运动认知；第二遍按时间轴核对动作变化、说话内容和切镜边界。
2. 先在内部完成全量语音转写并确认每句话的原意，再按“爆款二创复刻”目标重写画面口播。最终口播不要求逐字照抄原声，应删除重复词、无效语气词和平台操作话术，压缩成自然、接地气、可直接拍摄的短句；但必须保留原视频的核心营销逻辑与顺序，严禁改变商品、事件因果和卖点。
3. 以“明显切镜、主体动作阶段变化、商品展示阶段变化、口播语义段落变化”为分镜边界。分镜数量由素材决定，通常为 3–6 个，不要套用固定数量或按固定秒数机械切分。
4. 每个分镜分别核对人物位置、手部动作、商品状态、视线、表情、景别、构图、背景和光线。动作必须写成可执行的连续动作，不能只写抽象情绪。
5. 分镜时长应贴合真实动作边界，可使用整数或 0.5 秒粒度；所有分镜时长之和应与视频总时长基本一致，允许不超过 0.5 秒的取整误差。
6. 先从多帧一致证据提取全局设定，再写分镜差异。只写画面或声音中能够验证的具体事实；看不清的品牌文字不要臆造。
7. 根据素材自由选择最有感染力的叙事结构。可以采用“冲突—证据—反转—转化”，也可以采用悬念、对比、体验证明、场景代入或其他更适合原片的结构；不要为了套模板改变画面真实的动作顺序。

## 写作口径
- 目标是“复刻提示词”，所以描述应具体、稳定、可执行，包含人物外貌与服装、商品材质与包装、空间层次、光源方向、真实感、口音和情绪。
- 二创口播使用符合人物身份和场景的自然说话方式。允许压缩、重组或改写原话，也允许保留有感染力的口语词；可将平台操作指令改成更自然的营销号召，但不能捏造与画面、商品和原始营销意图相冲突的事实。
- 人物年龄使用合理年龄段，不要伪造精确年龄。人物族裔、服装、Logo、商品颜色与材质必须来自可见证据。
- “镜头视角”必须说明机位高度、固定或运动、是否手持；不要把普通正面拍摄误写成第一人称。
- “人物神态”既要写原片可见情绪，也必须补充复刻所需的真人感控制：自然微表情、每 3–4 秒自然眨眼、眼球缓慢转动、目光有神、嘴角和面部肌肉放松；杜绝面瘫脸、机械假脸和五官僵硬，去除 AI 虚拟质感。
- “画质要求”写复刻成片的交付标准，统一要求 1080P 高清、原生相机实拍质感、色彩真实、无过度滤镜、无美颜磨皮，并保留场景真实纹理；不要在最终结果里报告输入文件的技术分辨率。
- 全局约束条件使用以下生产约束：人物动作自然流畅，面部无扭曲变形；场景真实有生活感，无多余杂物穿模；精准还原商品外观，禁止错误生成；仅还原参考商品原有 LOGO 与印刷文字，禁止额外生成字幕、底部字幕、旁白字幕或任何新增文字，杜绝双层字幕。
- 水位、商品包装状态、人物与商品在画面中的左右位置、远处工作人员行为等空间细节必须逐镜核对，不能用“仓库背景”“展示商品”等泛化短语代替。
- 每个“人物动作描述”“画面构图”“背景环境描述”都要写出该镜头独有的可见细节，禁止用“同上”“同全局设定”等省略表达。
- 输出前检查分镜编号连续、必填字段完整、总时长合理，并确保每个镜头都有独立可执行的描述。结构要稳定，内容表达可以根据素材充分发散。
- 不输出置信度、推理过程、免责声明、改进建议或总结。

## 强制输出格式
严格按照下面的标题、字段名和顺序输出。除这份结果外不要输出任何前言或结语。每个分镜单独放在一个 Markdown 代码块中。

### 第一部分：全局基础设定
约束条件：...
人物形象：...
人物神态：...
商品形态：...
镜头视角：...
背景描述：...
光线分析：...
音色设定：...
画质要求：...
视频总时长：...秒

### 第二部分：分镜内容
\`\`\`
分镜 01
人物动作描述：...
画面口播文案：...
人物说话神态：...
音色语气设定：...
分镜时长：...秒
景别：...
画面构图：...
背景环境描述：...
光线风格分析：...
\`\`\`

继续用相同字段输出后续分镜，编号连续且不遗漏。`;
}
